package springmvc.springessentials.exodia.domain.models.views;

public class HomePageDocumentViewModel {

	private String id;
	private String title;
	
	public HomePageDocumentViewModel() {
	}
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getTitle() {
		return title;
	}
	
	public void setTitle(String title) {
		this.title = title;
	}
}
