package org.softuni.exam.domain.models.views;

public class HomePageDocumentViewModel {

	private String id;
	private String partialTitle;
	
	public HomePageDocumentViewModel() {
	}
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getPartialTitle() {
		return partialTitle;
	}
	
	public void setPartialTitle(String partialTitle) {
		this.partialTitle = partialTitle;
	}
}
